def main(args):
    """
    Main function to analyze genome coverage from pangenome and read mapping.
    
    Calculates:
    1. Percentage of genome represented in the pangenome (from BED file)
    2. Percentage of genome covered by read mapping (intersection with map_kmers keys)
    
    IMPORTANT: The BED file must be generated by the hvcf2bed function.
    This ensures reference coordinates are in columns 7-10.
    
    Returns coverage statistics for the entire genome and per-chromosome.
    """
    
    parser = argparse.ArgumentParser(
        prog="phgtools genome-intersection",
        description="Analyze genome intersection from map_kmers output and BED file with haplotype keys."
    )
    parser.add_argument("map_kmers_file", help="Path to the map_kmers output file")
    parser.add_argument("--bed-file", required=True, help="Path to the BED file generated by hvcf2bed (reference coordinates in columns 7-10)")
    parser.add_argument("--genome-fasta", required=True, help="Path to the reference genome FASTA file (used to get actual chromosome lengths)")
    parser.add_argument("-v", "--verbose", action="store_true", help="Enable verbose output")
    
    parsed_args = parser.parse_args(args)
    
    map_kmers_file = parsed_args.map_kmers_file
    bed_file = parsed_args.bed_file
    genome_fasta = parsed_args.genome_fasta
    verbose = parsed_args.verbose
    
    if verbose:
        print(f"Running the script with the following parameters:\n")
        print(f"Map kmers file: {map_kmers_file}\n")
        print(f"BED file: {bed_file}\n")
        print(f"Genome FASTA: {genome_fasta}\n")

    if not os.path.exists(map_kmers_file):
        print(f"File {map_kmers_file} does not exist")
        exit()
    if not os.path.exists(bed_file):
        print(f"File {bed_file} does not exist")
        exit()
    if not os.path.exists(genome_fasta):
        print(f"File {genome_fasta} does not exist")
        exit()

    # Load genome lengths from FASTA
    chr_lengths = LoadGenomeLengths(genome_fasta)
    if verbose:
        print(f"Loaded {len(chr_lengths)} chromosomes from FASTA file\n")
    
    # Extract genome name from BED file
    query_genome = ExtractGenomeFromBED(bed_file)
    
    # Load BED file directly into DataFrame (extracts reference coordinates)
    bed_df = LoadBEDToDataFrame(bed_file)
    if verbose:
        print(f"Loaded {len(bed_df)} regions from BED file\n")
    
    # Filter BED by map_kmers keys
    filtered_df = FilterBEDByMapKmers(bed_df, map_kmers_file)
    
    # Calculate and display coverage statistics
    CalculatePresentRanges(bed_df, filtered_df)
    
    # Calculate genome coverage using actual genome lengths
    coverage_results = CalculateGenomeCoverage(bed_df, filtered_df, chr_lengths)
    DisplayGenomeCoverage(coverage_results, verbose=verbose)


if __name__ == "__main__":
    import sys
    main(sys.argv[1:])

